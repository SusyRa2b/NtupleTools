double HT300_MHT80_v2_HT300_v9_170065_173211_sel1(std::vector<double>& inputvars,
                                                  int first=0,
                                                  int last=nnetworks-1)
{
  if ( first < 0 ) first = 0;
  if ( last  < 0 ) last  = nnetworks-1;
  if ( last  > nnetworks-1 ) last = nnetworks-1;
  if ( first > last )
    {
      std::cerr << "** HT300_MHT80_v2_HT300_v9_170065_173211_sel1 ** first-index > last-index"
                << std::endl; exit(3);
    }

  // Compute average over networks
  std::vector<double> in(ninputs);
  in[0] = (inputvars[0] - mean[0]) / sigma[0];

  int   n = last - first + 1;
  double x = 0.0;
  for(int i=first; i <= last; i++) x += netfun(in, weight[i]);
  return x / n;
}

