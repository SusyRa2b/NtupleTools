// Compute average over networks. Default is to use all networks
//-----------------------------------------------------------------------------

double ElHad_300_80(std::vector<double>& inputvars,
                    int first=0,
                    int last=nnetworks-1)
{
  if ( first < 0 ) first = 0;
  if ( last  < 0 ) last  = nnetworks-1;
  if ( last  > nnetworks-1 ) last = nnetworks-1;
  if ( first > last )
    {
      std::cerr << "** ElHad_300_80 ** first-index > last-index"
                << std::endl; exit(3);
    }

  // Compute average over networks
  std::vector<double> in(ninputs);
  in[0] = (inputvars[0] - mean[0]) / sigma[0];

  int   n = last - first + 1;
  double x = 0.0;
  for(int i=first; i <= last; i++) x += netfun(in, weight[i]);
  return x / n;
}

extern "C" double ElHad_300_80_dl(std::vector<double>& in,
                                  int first=0,
                                  int last=nnetworks-1)
{
  return ElHad_300_80(in, first, last);
}

extern "C" double ElHad_300_80_dl2(double* in,
                                   int first=0,
                                   int last=nnetworks-1)
{
  std::vector<double> invec;

  invec.push_back(in[0]);

  return ElHad_300_80(invec, first, last);
}
